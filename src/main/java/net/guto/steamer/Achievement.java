package net.guto.steamer;

public class Achievement {

	String iconClosed;
	String iconOpen;
	String name;
	String apiname;
	String description;

	public String getIconClosed() {
		return iconClosed;
	}

	public String getIconOpen() {
		return iconOpen;
	}

	public String getName() {
		return name;
	}

	public String getApiname() {
		return apiname;
	}

	public String getDescription() {
		return description;
	}
}
